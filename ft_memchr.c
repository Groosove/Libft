#include "libft.h"

void	*ft_memchr(const void *arr, int c, size_t size)
{
	char	*tmp;
	char	value;

	value = (char)c;
	tmp = (char*)arr;
	while (size-- != 0)
	{
		if (*tmp == value)
			return (tmp);
		tmp++;
	}
	return (0);
}
