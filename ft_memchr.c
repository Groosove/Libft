#include "libft.h"

void	*ft_memchr(const void *arr, int c, size_t size)
{
	char	*tmp;

	tmp = (char*)arr;
	while (size-- != 0)
		if (*tmp++ == c)
			return (tmp - 1);
	return (0);
}
