#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*tmp1;
	char	*tmp2;

	tmp1 = (char *)dst;
	tmp2 = (char *)src;
	if (dst == 0 && src == 0)
		return (0);
	while (n-- != 0)
		*tmp1++ = *tmp2++;
	return (dst);
}
