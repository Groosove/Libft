#include "libft.h"

char			*ft_strtrim(char const *s1, char const *set)
{
	size_t	size;

	if (set == 0 || s1 == 0)
		return (0);
	while (*s1 != 0 && ft_strchr(set, *s1))
		s1++;
	size = ft_strlen(s1);
	while (size && ft_strchr(set, s1[size]))
		size--;
	return (ft_substr((char *)s1, 0, size + 1));
}
